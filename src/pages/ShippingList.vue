<template>
     <div>
        <Navbar></Navbar>
        <Breadcrumb></Breadcrumb>
        <div class="row justify-content-center">
            <div class="col-10">
                <ShippingTable v-show="!viewForm" @updateShippingFunc="updateShippingFunc"></ShippingTable>
                <ShipForm v-show="viewForm" :shippingDataProps="shippingData"></ShipForm>
            </div>
        </div>
        <Footer></Footer>
        <Copyright></Copyright>
    </div>
</template>

<script>
    import Navbar from "../components/Navbar.vue"
    import Breadcrumb from "../components/Breadcrumb.vue"
    import Footer from "../components/Footer.vue"
    import Copyright from "../components/Copyright.vue"
    import ShipForm from "../components/Shipform.vue"
    import ShippingTable from "../components/ShippingTable.vue"

export default {
    name: "ShippingList",
        components: {
            Navbar, 
            Breadcrumb,
            Footer,
            Copyright,
            ShippingTable,
            ShipForm
        },
        methods: {
            updateShippingFunc(item){
                this.shippingData = item
                this.viewForm = true;
            }
        },
        data(){
            return{
                viewForm : false,
                shippingData: null
            }
        }
}
</script>

